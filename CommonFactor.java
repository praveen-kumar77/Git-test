package com.java;

import java.util.Arrays;
import java.util.HashSet;
import java.util.LinkedHashSet;
import java.util.Set;

public class CommonFactor {
    public static void main(String[] args) {
        String word1 = "TAUXXTAUXXTAUXXTAUXXTAUXX";
        String word2 = "TAUXXTAUXXTAUXXTAUXXTAUXXTAUXXTAUXXTAUXXTAUXX";
        StringBuilder answer = new StringBuilder();
        Set<Character> set1 = new LinkedHashSet<>();
        Set<Character> set2 = new LinkedHashSet<>();

        for(char i : word1.toCharArray()){
            set1.add(i);
        }
        for(char i : word2.toCharArray()){
            set2.add(i);
        }
        if(set1.equals(set2)){
            for(char i : set2){
                answer.append(i);
            }
        }
        System.out.println(answer);
    }
}
